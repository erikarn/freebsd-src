/**
 * @brief Calculate the PLL frequency.
 */
uint64_t
qcom_clk_apll_calc_freq(uint64_t parent_rate, uint32_t l, uint32_t a,
    uint32_t alpha_width)
{

	return ((parent_rate * l) +
	    ((parent_rate * a) >> QCOM_CLK_APLL_ALPHA_SHIFT(alpha_width)));
}
